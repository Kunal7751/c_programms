#include<stdio.h>
int main(){
	int num1 = 10;
	float num2 = 10.20;
	double num3 = 2050.3020;
	char num4 = 'A';
	char num5[] = "Ram";
	 
	printf("this is Int : %d \n",num1);
	printf("this is float : %f \n" ,num2);
	printf("this is double : %lf \n",num3);
	printf("this is char : %c \n",num4);
	printf("this is string : %s \n",num5);
	return 0;
}